/**
 * 根据国家ISO2代码获取数字区号
 * @param {string} iso2Code - 国家ISO2代码
 * @returns {string} - 数字区号，默认返回'86'（中国）
 */
export function getCountryCode(iso2Code) {
  return countryCodeMap[iso2Code] || '86'
}

/**
 * 根据数字区号获取国家ISO2代码
 * @param {string} countryCode - 数字区号
 * @returns {string} - 国家ISO2代码，默认返回'cn'（中国）
 */
export function getCountryByCode(countryCode) {
  for (const [iso2, code] of Object.entries(countryCodeMap)) {
    if (code === countryCode) {
      return iso2
    }
  }
  return 'cn'
}

/**
 * 根据国家ISO2代码获取国家名称
 * @param {string} iso2Code - 国家ISO2代码
 * @returns {string} - 国家名称，默认返回'中国'
 */
export function getCountryName(iso2Code) {
  return countryNameMap[iso2Code] || '中国'
}

/**
 * 获取所有支持的国家代码映射
 * @returns {Object} - 国家代码映射对象
 */
